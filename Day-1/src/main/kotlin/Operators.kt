fun main(){
//    operators like '+' , '-' , '/' , '%' , '*'
    val num1 = 10
    val num2 = 5

    println("$num1 + $num2 = ${num1 + num2} ")
    println("$num1 - $num2 = ${num1 - num2} ")
    println("$num1 * $num2 = ${num1 * num2} ")
    println("$num1 / $num2 = ${num1 / num2} ")
    println("$num1 % $num2 = ${num1 % num2} ")
}
